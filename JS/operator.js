//typeOf operator
let num1=5;
let num2=2;
console.log(typeof num1);

// instanceof....used for array or object 
let arr=[1 ,3,5,7];
console.log(arr instanceof Array) //true
console.log(arr instanceof Object) //true (Array নিজেই Object থেকে extend হয়েছে।)

// Addition
console.log(num1 + num2);

// Subtraction
console.log(num1 - num2);

// Multiplication
console.log(num1 * num2)

// Division
console.log(num1 / num2)

// Modulus
console.log(num1 % num2)

// Exponent
console.log(num1 ** num2)

